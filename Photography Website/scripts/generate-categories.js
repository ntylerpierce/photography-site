// Run from the project root: node scripts/generate-categories.js

const fs = require('fs');
const path = require('path');

const IMAGES_DIR = path.join(__dirname, '..', 'images');
const OUTPUT_FILE = path.join(__dirname, 'categories.js');
const IMAGE_EXTS = new Set(['.jpg', '.jpeg', '.png', '.webp', '.gif', '.avif']);

function toLabel(folderName) {
  return folderName
    .replace(/[-_]/g, ' ')
    .replace(/\b\w/g, c => c.toUpperCase());
}

function toTitle(filename) {
  const base = path.basename(filename, path.extname(filename));
  // Preserve camera-generated names (e.g. DSC00197) as-is; humanize others
  if (/^[A-Z]{1,5}\d+$/i.test(base)) return '';
  return base
    .replace(/[-_]/g, ' ')
    .replace(/\b\w/g, c => c.toUpperCase());
}

if (!fs.existsSync(IMAGES_DIR)) {
  console.error(`images/ directory not found at: ${IMAGES_DIR}`);
  process.exit(1);
}

const categoryFolders = fs.readdirSync(IMAGES_DIR, { withFileTypes: true })
  .filter(d => d.isDirectory())
  .map(d => d.name)
  .sort();

if (categoryFolders.length === 0) {
  console.error('No category subfolders found inside images/');
  process.exit(1);
}

const categories = categoryFolders.map(folder => {
  const folderPath = path.join(IMAGES_DIR, folder);
  const files = fs.readdirSync(folderPath)
    .filter(f => IMAGE_EXTS.has(path.extname(f).toLowerCase()))
    .sort();

  // Prefer a file explicitly named "cover.*"; otherwise use the first image
  const coverFile = files.find(f => /^cover\./i.test(f)) || files[0] || '';

  const photos = files
    .filter(f => !/^cover\./i.test(f))  // cover isn't also a gallery photo
    .map(f => ({
      src: `images/${folder}/${f}`,
      title: toTitle(f),
      date: '',
    }));

  return {
    id: folder,
    label: toLabel(folder),
    cover: coverFile ? `images/${folder}/${coverFile}` : '',
    photos,
  };
});

// --- Build output ---
const lines = [
  `// Auto-generated by scripts/generate-categories.js — do not edit by hand.`,
  `// Run: node scripts/generate-categories.js`,
  ``,
  `const CATEGORIES = [`,
];

for (const cat of categories) {
  lines.push(`  {`);
  lines.push(`    id: "${cat.id}",`);
  lines.push(`    label: "${cat.label}",`);
  lines.push(`    cover: "${cat.cover}",`);
  lines.push(`    photos: [`);
  for (const p of cat.photos) {
    const titlePart = p.title ? `, title: "${p.title}"` : ``;
    lines.push(`      { src: "${p.src}"${titlePart}, date: "" },`);
  }
  lines.push(`    ]`);
  lines.push(`  },`);
}

lines.push(`];`);
lines.push(``);

fs.writeFileSync(OUTPUT_FILE, lines.join('\n'), 'utf8');

console.log(`\nWrote ${OUTPUT_FILE}\n`);
categories.forEach(c => {
  const status = c.photos.length === 0 ? ' (no images found)' : '';
  console.log(`  ${c.id}: ${c.photos.length} photos, cover: ${c.cover || 'none'}${status}`);
});
console.log('');
